function CD = cindir(q)
l1a=16; l1b=9; l2=9; l4=1; l5=1; l6=1;
q1=q(1);
q2=q(2);
q3=q(3);
q4=q(4);
q5=q(5);
q6=q(6);

DH = [[q1,l1a,-l1b,0];...
      [q2-(pi/2),0,-l2,pi];...
      [-pi/2,q3+l4,0,0];...
      [q4,0,0,-pi/2];...
      [q5+pi/2,0,0,pi/2];...
      [q6,l5+l6,0,0]];
 
A1 = ( D_H(DH(1,:)) );
A2 = ( D_H(DH(2,:)) );
A3 = ( D_H(DH(3,:)) );
A4 = ( D_H(DH(4,:)) );
A5 = ( D_H(DH(5,:)) );
A6 = ( D_H(DH(6,:)) );
 
T01 = A1;
T02 = A1*A2;
T03 = (A1*A2*A3);
T04 = A1*A2*A3*A4;
T05 = A1*A2*A3*A4*A5;
T06 = A1*A2*A3*A4*A5*A6;
 
% T36 = (A4*A5*A6);
%  
% T06 = (T03*T36);
CD(:,:,1)=T01;
CD(:,:,2)=T02;
CD(:,:,3)=T03;
CD(:,:,4)=T04;
CD(:,:,5)=T05;
CD(:,:,6)=T06;

CD;
 

